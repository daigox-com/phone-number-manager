    /**
     * Get all country calling codes as an array
     * @return array
     */
    public static function getAllCodes(): array
    {
        return array_values((new \ReflectionClass(__CLASS__))->getConstants());
    }

    /**
     * Get country calling code by country name
     * @param string $countryName
     * @return string|null
     */
    public static function getCodeByCountry(string $countryName): ?string
    {
        $constantName = strtoupper(str_replace(' ', '_', $countryName));
        return defined("self::$constantName") ? constant("self::$constantName") : null;
    }
